package ddw.vo;

public class ProdVO {
	
	private String prod_num;
	private String prod_nm ;
	private String prod_con;
	private String prod_pa ;
	private String lprod_gu;
	private String mem_id;
	
	
	/**
	 * @return the mem_id
	 */
	public String getMem_id() {
		return mem_id;
	}
	/**
	 * @param mem_id the mem_id to set
	 */
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getProd_num() {
		return prod_num;
	}
	public void setProd_num(String prod_num) {
		this.prod_num = prod_num;
	}
	public String getProd_nm() {
		return prod_nm;
	}
	public void setProd_nm(String prod_nm) {
		this.prod_nm = prod_nm;
	}
	public String getProd_con() {
		return prod_con;
	}
	public void setProd_con(String prod_con) {
		this.prod_con = prod_con;
	}
	public String getProd_pa() {
		return prod_pa;
	}
	public void setProd_pa(String prod_pa) {
		this.prod_pa = prod_pa;
	}
	public String getLprod_gu() {
		return lprod_gu;
	}
	public void setLprod_gu(String lprod_gu) {
		this.lprod_gu = lprod_gu;
	}

                          
}
