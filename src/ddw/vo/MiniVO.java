package ddw.vo;

public class MiniVO {
	private String dd_add;
	private double mini_left;
	private double mini_top;
	private String prod_num;
	private String prod_nm;
	private String mini_type;
	
	public String getDd_add() {
		return dd_add;
	}
	public void setDd_add(String dd_add) {
		this.dd_add = dd_add;
	}
	public double getMini_left() {
		return mini_left;
	}
	public void setMini_left(double mini_left) {
		this.mini_left = mini_left;
	}
	public double getMini_top() {
		return mini_top;
	}
	public void setMini_top(double mini_top) {
		this.mini_top = mini_top;
	}
	public String getProd_num() {
		return prod_num;
	}
	public void setProd_num(String prod_num) {
		this.prod_num = prod_num;
	}
	public String getProd_nm() {
		return prod_nm;
	}
	public void setProd_nm(String prod_nm) {
		this.prod_nm = prod_nm;
	}
	public String getMini_type() {
		return mini_type;
	}
	public void setMini_type(String mini_type) {
		this.mini_type = mini_type;
	}
	
	
	
}
