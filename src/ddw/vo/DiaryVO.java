package ddw.vo;

public class DiaryVO {
	private int diary_no;
	private String dd_add;
	private String diary_cont;
	private String diary_date;
	private String diary_type;
	
	public int getDiary_no() {
		return diary_no;
	}
	public void setDiary_no(int diary_no) {
		this.diary_no = diary_no;
	}
	public String getDd_add() {
		return dd_add;
	}
	public void setDd_add(String dd_add) {
		this.dd_add = dd_add;
	}
	public String getDiary_cont() {
		return diary_cont;
	}
	public void setDiary_cont(String diary_cont) {
		this.diary_cont = diary_cont;
	}
	public String getDiary_date() {
		return diary_date;
	}
	public void setDiary_date(String diary_date) {
		this.diary_date = diary_date;
	}
	public String getDiary_type() {
		return diary_type;
	}
	public void setDiary_type(String diary_type) {
		this.diary_type = diary_type;
	}
	
	
}
